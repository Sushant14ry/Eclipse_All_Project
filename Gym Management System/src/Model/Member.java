package Model;

public class Member {
	
	private int mid;
	private String Fullname;
	private String MobileNumber;
	private String Emailid;
	private String Gender;
	private String Fathername;
	private String Password;
	private String Gymtime;
	private String CitizenNum;
	private int Age;
	private int Amountpay;
	
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public String getFullname() {
		return Fullname;
	}
	public void setFullname(String fullname) {
		Fullname = fullname;
	}
	public String getMobileNumber() {
		return MobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		MobileNumber = mobileNumber;
	}
	public String getEmailid() {
		return Emailid;
	}
	public void setEmailid(String emailid) {
		Emailid = emailid;
	}
	public String getGender() {
		return Gender;
	}
	public void setGender(String gender) {
		Gender = gender;
	}
	public String getFathername() {
		return Fathername;
	}
	public void setFathername(String fathername) {
		Fathername = fathername;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getGymtime() {
		return Gymtime;
	}
	public void setGymtime(String gymtime) {
		Gymtime = gymtime;
	}
	public String getCitizenNum() {
		return CitizenNum;
	}
	public void setCitizenNum(String citizenNum) {
		CitizenNum = citizenNum;
	}
	public int getAge() {
		return Age;
	}
	public void setAge(int age) {
		Age = age;
	}
	public int getAmountpay() {
		return Amountpay;
	}
	public void setAmountpay(int amountpay) {
		Amountpay = amountpay;
	}
	
	
}
