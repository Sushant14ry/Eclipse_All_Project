package model;

public class Deposite {
	private int did;
	private String acc_no;
	private String fullname;
	private String username;
	private String number;
	private int av_balance;
	private String dep_name;
	private String dep_num;
	private int dep_amount;
	private int total_amount;
	
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public String getAcc_no() {
		return acc_no;
	}
	public void setAcc_no(String acc_no) {
		this.acc_no = acc_no;
	}
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public int getAv_balance() {
		return av_balance;
	}
	public void setAv_balance(int av_balance) {
		this.av_balance = av_balance;
	}
	public String getDep_name() {
		return dep_name;
	}
	public void setDep_name(String dep_name) {
		this.dep_name = dep_name;
	}
	public String getDep_num() {
		return dep_num;
	}
	public void setDep_num(String dep_num) {
		this.dep_num = dep_num;
	}
	public int getDep_amount() {
		return dep_amount;
	}
	public void setDep_amount(int dep_amount) {
		this.dep_amount = dep_amount;
	}
	public int getTotal_amount() {
		return total_amount;
	}
	public void setTotal_amount(int total_amount) {
		this.total_amount = total_amount;
	}
	
	
	
}
